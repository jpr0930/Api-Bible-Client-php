<?php

namespace ApiBibleClient\Api\Model;

/**
 * Class Language
 * @package ApiBibleClient\Api\Model
 */
final class Language {

    /** @var string */
    private $id;
    /** @var string */
    private $name;
    /** @var string */
    private $nameLocal;
    /** @var string */
    private $script;
    /** @var string */
    private $scriptDirection;

    /**
     * Language constructor.
     * @param string $id
     * @param string $name
     * @param string $nameLocal
     * @param string $script
     * @param string $scriptDirection
     */
    public function __construct(
        string $id,
        string $name,
        string $nameLocal,
        string $script,
        string $scriptDirection
    ) {
        $this->id              = $id;
        $this->name            = $name;
        $this->nameLocal       = $nameLocal;
        $this->script          = $script;
        $this->scriptDirection = $scriptDirection;
    }

    /**
     * @param array $languageData
     * @return static
     */
    public static function createFromArray(array $languageData): self {
        return new static(
            $languageData['id'],
            $languageData['name'],
            $languageData['nameLocal'],
            $languageData['script'],
            $languageData['scriptDirection'],
        );
    }

    /**
     * @return string
     */
    public function getId(): string {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName(): string {
        return $this->name;
    }

    /**
     * @return string
     */
    public function getNameLocal(): string {
        return $this->nameLocal;
    }

    /**
     * @return string
     */
    public function getScript(): string {
        return $this->script;
    }

    /**
     * @return string
     */
    public function getScriptDirection(): string {
        return $this->scriptDirection;
    }


}